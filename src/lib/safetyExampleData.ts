/**
 * Шаблон строки. Служебные поля заполнены значениями по умолчанию
 * ("в наличии" / "соответствует" / "отсутствуют" / "соответствует стандартам").
 * `equipment` намеренно ПУСТОЙ — оборудование пользователь вписывает вручную
 * (поле необязательное), никакой автоподстановки. Те же значения, что и у
 * новых строк при Sync (SAFETY_NEW_ROW_DEFAULTS).
 */
function row(code: string, position: string): SafetyRow {
  return {
    code,
    position,
    count: 1,
    equipment: "",
    documentation: "в наличии",
    result: "соответствует",
    nonComplianceReasons: "отсутствуют",
    finalNote: "соответствует стандартам",
  };
}

