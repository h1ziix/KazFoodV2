/**
 * Форматирование порядкового номера протокола для шапки «ПРОТОКОЛ № …».
 *
 * Правило (требование заказчика 2026-06-15): всегда одна ведущая «0»,
 * за ней само число с минимумом в 2 цифры. Иначе говоря — ширина числовой
 * части не меньше 3 символов, и всегда есть хотя бы один ведущий ноль:
 *
 *   1   → "001"      9   → "009"
 *   10  → "010"      99  → "099"
 *   100 → "0100"     111 → "0111"
 *   1000 → "01000"
 *
 * Нумерация СВОЯ в каждом протоколе (начинается с 001), порядковый номер
 * берётся из позиции рабочего места в документе.
 */
export function formatProtocolNumber(seq: number): string {
  if (!Number.isInteger(seq) || seq < 1) {
    throw new Error(
      `formatProtocolNumber: ожидался положительный целый номер, получено ${seq}`,
    );
  }
  return "0" + String(seq).padStart(2, "0");
}
