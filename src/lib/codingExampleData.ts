import type { CodingProtocol, CodingRow } from "@/types/coding";

/**
 * Коды в примере КАНОНИЧЕСКИЕ: "01" + номер раздела + порядковый номер
 * СТРОКИ в разделе (третий блок начинается с 001 в каждом разделе).
 * Количество на код не влияет и может быть только 0 или 1: повторяющиеся
 * должности заведены отдельными строками — например, два «Менеджера по
 * продажам» это строки 006 и 007. Стабильные id строкам не прописаны —
 * их назначает normalizeCodingDocument при сидинге.
 */
function r(code: string, name: string, count = 1): CodingRow {
  return { code, name, count };
}

