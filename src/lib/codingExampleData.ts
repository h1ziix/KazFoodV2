import type { CodingProtocol, CodingRow } from "@/types/coding";

/**
 * Коды в примере КАНОНИЧЕСКИЕ: "01" + номер раздела + позиция строки в
 * разделе (третий блок начинается с 001 в каждом разделе). Стабильные id
 * строкам не прописаны — их назначает normalizeCodingDocument при сидинге.
 */
function r(code: string, name: string, count = 1): CodingRow {
  return { code, name, count };
}

