import type { CodingProtocol, CodingRow } from "@/types/coding";

/**
 * Коды в примере КАНОНИЧЕСКИЕ: "01" + номер раздела + номер первого
 * РАБОЧЕГО МЕСТА строки (третий блок начинается с 001 в каждом разделе).
 * Строка с count = N занимает N последовательных номеров, поэтому после
 * «Менеджер по продажам, 2» (006–007) следующая строка получает 008.
 * Стабильные id строкам не прописаны — их назначает
 * normalizeCodingDocument при сидинге.
 */
function r(code: string, name: string, count = 1): CodingRow {
  return { code, name, count };
}

