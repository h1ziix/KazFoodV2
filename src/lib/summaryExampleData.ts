const GOST_LIGHT = "ГОСТ 24940-96";
const GOST_TH = "ГОСТ 12.1.005-88";
const GOST_NOISE = "ГОСТ ISO 9612-2016";
const GOST_EMF = "ГОСТ 12.1006-84";

type FacInit = {
  name: string;
  method: string;
  norm: string;
  actual: string;
  classValue?: SummaryFactor["classValue"];
};

function f(init: FacInit): SummaryFactor {
  return {
    name: init.name,
    method: init.method,
    norm: init.norm,
    actual: init.actual,
    classValue: init.classValue ?? "",
  };
}

/** Полный набор факторов для офисного места (АУП): 8 факторов. */
function officeFactors(
  light: string,
  temp: string,
  hum: string,
  noise: string,
  ep1: string,
  ep2: string,
  mp1: string,
  mp2: string,
): SummaryFactor[] {
  return [
    f({ name: "Освещение, лк", method: GOST_LIGHT, norm: "300", actual: light, classValue: "2" }),
    f({ name: "Температура, ºС", method: GOST_TH, norm: "21-27", actual: temp, classValue: "2" }),
    f({ name: "Влажность, %", method: GOST_TH, norm: "60", actual: hum, classValue: "2" }),
    f({ name: "Шум, дБа", method: GOST_NOISE, norm: "50", actual: noise, classValue: "2" }),
    f({ name: "ЭП диапазон 1, В/м", method: GOST_EMF, norm: "25", actual: ep1, classValue: "2" }),
    f({ name: "ЭП диапазон 2, В/м", method: GOST_EMF, norm: "2,5", actual: ep2, classValue: "2" }),
    f({ name: "МП диапазон 1, мкТл", method: GOST_EMF, norm: "250", actual: mp1 }),
    f({ name: "МП диапазон 2, нТл", method: GOST_EMF, norm: "25", actual: mp2 }),
  ];
}

/** Производственное место с полным набором (свет/темп/влажн/шум). */
function prodFactors(
  light: string,
  lightNorm: string,
  temp: string,
  hum: string,
  noise: string,
  noiseNorm: string,
  humNorm: string,
  tempNorm: string,
): SummaryFactor[] {
  return [
    f({ name: "Освещение, лк", method: GOST_LIGHT, norm: lightNorm, actual: light, classValue: "2" }),
    f({ name: "Температура, ºС", method: GOST_TH, norm: tempNorm, actual: temp, classValue: "2" }),
    f({ name: "Влажность, %", method: GOST_TH, norm: humNorm, actual: hum, classValue: "2" }),
    f({ name: "Шум, дБа", method: GOST_NOISE, norm: noiseNorm, actual: noise, classValue: "2" }),
  ];
}

function wp(
  code: string,
  profession: string,
  count: number,
  factors: SummaryFactor[],
): SummaryWorkplace {
  return { code, profession, count, factors };
}

