/**
 * Документ №1 — «Обложка» отчёта об аттестации рабочих мест.
 *
 * Исходный DOCX состоит из двух одинаковых половин (одна и та же
 * вёрстка повторяется дважды — kk-KZ и ru-RU). В каждой половине
 * фиксированный титульный блок ("АТТЕСТАЦИЯ РАБОЧИХ МЕСТ…") и одна
 * таблица с подписями: заказчик слева, исполнитель справа. Затем
 * строки с городом и годом "Алматы 2026 г." (год отчёта) и
 * "Алматы 2020 г." (год учреждения лаборатории).
 *
 * Здесь нет loops/индикаторов/секций — это плоский набор скаляров.
 */

export interface CoverDocument {
  /** Заказчик (организация и её директор, левая колонка подписей). */
  customer: {
    /** Полное наименование, напр. "ТОО  «KazEcoFood»". */
    organization: string;
    /** ФИО директора заказчика, напр. "Балян  Л.Н.". */
    directorName: string;
  };

  /** Исполнитель работ (организация и её директор, правая колонка). */
  performer: {
    /** Полное наименование, напр. "ТОО «Центр экспертной оценки условий труда»". */
    organization: string;
    /** Должность подписанта исполнителя, напр. "Генеральный директор". */
    directorPosition: string;
    /** ФИО директора исполнителя, напр. "Дьяченко В. Г.". */
    directorName: string;
  };

  /** Город оформления обложки, напр. "Алматы". */
  city: string;

  /** Год отчёта об аттестации, напр. "2026". */
  reportYear: string;

  /** Год учреждения/аккредитации лаборатории (нижняя строка), напр. "2020". */
  archiveYear: string;
}
