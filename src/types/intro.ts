/**
 * Документ №2 — «Введение» (двуязычный титульный текст отчёта об
 * аттестации рабочих мест: казахская + русская версии).
 *
 * Исходный DOCX в основном состоит из дублирующегося казахско-русского
 * нормативного boilerplate (содержание, общие сведения, список из 14
 * нормативных документов, описание системы безопасности и охраны труда).
 * Параметризуем только переменные скаляры: реквизиты заказчика и
 * исполнителя, дату измерений, количество рабочих мест и сотрудников,
 * распределение по классам тяжести/напряжённости и итоговый класс
 * травмобезопасности.
 *
 * Тут нет loops/секций/индикаторов в смысле других протоколов — это
 * плоский set скаляров плюс две вложенные группы счётчиков по классам.
 */

export interface IntroDocument {
  /** Реквизиты заказчика аттестации. */
  customer: {
    /** Краткое юридическое имя без кавычек, напр. "KazEcoFood". */
    name: string;
    /** Город, упоминаемый рядом с заказчиком, напр. "Алматы". */
    city: string;
    /**
     * Полный юридический адрес заказчика без «ТОО «<name>»,», напр.
     * "Алманиская обл, Карасайский район, село Кокозек, улица Несибели, 715".
     */
    address: string;
  };

  /** Дата проведения аттестации в формате «<day>» <month> <year> г. */
  measurementDate: {
    /** День, напр. "10". */
    day: string;
    /** Месяц в родительном падеже, напр. "апреля". */
    month: string;
    /** Год, напр. "2026". */
    year: string;
  };

  /** Количество рабочих мест, подлежащих аттестации, напр. 55. */
  workplaceCount: number;
  /** Число мужчин среди аттестуемых сотрудников. */
  maleCount: number;
  /** Число женщин среди аттестуемых сотрудников. */
  femaleCount: number;

  /** Реквизиты исполнителя (лаборатории). */
  performer: {
    /** Полное наименование, напр. "ТОО «Центр экспертной оценки условий труда»". */
    organization: string;
    /**
     * Адрес лаборатории (русская версия), идущий в строку «Адрес
     * испытательной лаборатории: …».
     */
    addressRu: string;
    /**
     * Адрес лаборатории в казахской записи, идущий в параллельную
     * казахскую строку «Зертхана мекенжайы: …».
     */
    addressKk: string;
    /**
     * Реквизиты аттестата аккредитации лаборатории, общие для
     * казахско-русского блока.
     */
    accreditation: {
      /** Номер аттестата, напр. "KZ.T.02.Е 1210". */
      number: string;
      /**
       * Дата выдачи в русской записи, напр. "25 июля 2022 года".
       */
      dateRu: string;
      /**
       * Дата выдачи в казахской записи, напр. "2022 жылғы 25 шілдедегі".
       */
      dateKk: string;
    };
  };

  /**
   * Сводное распределение рабочих мест по классам тяжести трудового
   * процесса. Используются три из четырёх стандартных классов:
   * оптимальный (1), допустимый (2), вредный 3.1.
   */
  heavinessCounts: {
    /** Класс «оптимальный 1». */
    c1: number;
    /** Класс «допустимый 2». */
    c2: number;
    /** Класс «вредный 3.1». */
    c31: number;
  };

  /** Аналогичное распределение по классам напряжённости трудового процесса. */
  tensionCounts: {
    c1: number;
    c2: number;
    c31: number;
  };

  /**
   * Итоговый класс травмобезопасности с человекочитаемым именем класса,
   * напр. "допустимый 2".
   */
  safetyClassLabel: string;
}
