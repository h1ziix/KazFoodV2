/**
 * Документ №13 — «Оценка обеспеченности средствами индивидуальной защиты (СИЗ)».
 *
 * Исходный DOCX — одна большая таблица, в которой строки сгруппированы
 * по разделам:
 *   «1. Администрация – 3 рабочих мест»
 *   «2. Производственный персонал»
 *
 * Структура колонок данных:
 *   1) код рабочего места
 *   2) наименование профессии/должности
 *   3) количество рабочих мест
 *   4) перечень СИЗ по нормам (normItems)
 *   5) фактически выдано (issuedFact: "Да" / "Нет" / "-")
 *   6) ГОСТ / наличие сертификата (certificate: "В наличии" / "-")
 *   7) оценка обеспеченности (assessment: "Обеспечен" / "Необеспечен" / "-")
 *   8) примечание (note)
 *
 * Структура данных:
 *   SizProtocol → sections[] → rows[]
 */

/** Одна строка таблицы — одно рабочее место. */
export interface SizRow {
  /** Код рабочего места, напр. "01 001 001". */
  code: string;
  /** Наименование профессии/должности. */
  position: string;
  /** Количество рабочих мест данной профессии. */
  count: number;
  /**
   * Перечень СИЗ согласно действующим нормам. Для административного
   * персонала чаще всего длинная фраза вида
   * «- не предусмотрено, согласно "Нормам…", Приказ № 943».
   */
  normItems: string;
  /** Фактически выдано: "Да" / "Нет" / "-". */
  issuedFact: string;
  /** ГОСТ / наличие сертификата: "В наличии" / "-". */
  certificate: string;
  /** Оценка обеспеченности: "Обеспечен" / "Необеспечен" / "-". */
  assessment: string;
  /** Примечание (часто "-"). */
  note: string;
}

/** Раздел таблицы — группа строк под общим заголовком. */
export interface SizSection {
  /** Порядковый номер раздела (1, 2, …). */
  number: number;
  /** Заголовок раздела, напр. "1. Администрация – 3 рабочих мест". */
  title: string;
  /** Строки рабочих мест внутри раздела. */
  rows: SizRow[];
}

export interface SizProtocol {
  protocol: {
    /** Номер протокола, напр. "1". */
    number: string;
  };
  customer: {
    name: string;
    address: string;
  };
  /** Место проведения оценки. */
  measurementPlace: string;
  measurementDate: {
    day: string;
    month: string;
    year: string;
  };
  /** Разделы таблицы. */
  sections: SizSection[];
  performer: {
    fullName: string;
    position: string;
  };
  representative: {
    fullName: string;
    position: string;
  };
}
