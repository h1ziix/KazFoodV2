/**
 * Документ №9 — «Сводный протокол вредности».
 *
 * Исходный DOCX (`9. Протокол сводный вредности KAZFOOD.docx`) состоит из:
 *   1. Шапка протокола (номер, даты, заказчик, место, оборудование,
 *      условия среды и т.д.).
 *   2. Таблица средств измерений (5 поверочных приборов).
 *   3. Главная таблица «Результаты измерений вредных производственных
 *      факторов» (13 колонок). Строки сгруппированы заголовками-
 *      разделами вида «1. Административно – управленческий персонал» /
 *      «2. Производственный персонал». Каждое рабочее место занимает
 *      несколько подряд идущих строк — по строке на каждый измеренный
 *      фактор (освещение, температура, влажность, шум, ЭП₁, ЭП₂, МП₁,
 *      МП₂ и т.п.). В первой строке рабочего места заполнены колонки
 *      «Код рабочего места», «Профессия/должность», «Количество мест»;
 *      в последующих — только данные фактора.
 *   4. Подписи (исполнитель + представитель организации).
 *
 * Структура модели:
 *   SummaryProtocol
 *     ├─ protocol / customer / measurementDate / ...
 *     ├─ measuringTools[]          — табл. поверки СИ
 *     └─ places[] → workplaces[] → factors[]
 */

/** Один прибор/средство измерения из таблицы СИ. */
export interface SummaryMeasuringTool {
  /** Порядковый № в таблице СИ (колонка 1). */
  rowNumber: number;
  /** Наименование СИ (колонка 2). */
  name: string;
  /** № сертификата о поверке (колонка 3). */
  certificate: string;
  /** Дата поверки / срок действия (колонка 4). */
  verificationDate: string;
}

/**
 * Одна строка таблицы измерений = один производственный фактор
 * на конкретном рабочем месте.
 *
 * Класс условий труда фиксируется один из шести: «2» (допустимый),
 * «3.1», «3.2», «3.3», «3.4» (вредный) или «4» (опасный). Хранится
 * именно символьное значение — генератор сам поместит его в нужную
 * колонку таблицы.
 */
export interface SummaryFactor {
  /** Наименование фактора с единицей измерения, напр. "Освещение, лк". */
  name: string;
  /** НД на метод измерения, напр. "ГОСТ 24940-96". */
  method: string;
  /** Норма / ПДК / ПДУ, напр. "300", "21-27", "60". */
  norm: string;
  /** Фактический уровень, напр. "461", "23,5", "-". */
  actual: string;
  /**
   * Класс условий труда. Один из: "2" | "3.1" | "3.2" | "3.3" | "3.4" | "4".
   * Пустая строка допускается для строк без оценки класса (ЭП/МП).
   */
  classValue: "" | "2" | "3.1" | "3.2" | "3.3" | "3.4" | "4";
}

/** Рабочее место — набор измеренных факторов под общим кодом/должностью. */
export interface SummaryWorkplace {
  /**
   * Стабильный id строки кодировки (CodingRow.id) — первичный ключ матчинга
   * при синхронизации. Скрыт из формы; легаси-строки без него матчатся по коду.
   */
  codingRowId?: string;
  /** Код рабочего места, напр. "01 001 001" — отображаемое производное
   *  значение из кодировки, обновляется при синхронизации. */
  code: string;
  /** Наименование профессии/должности, напр. "Директор". */
  profession: string;
  /** Количество рабочих мест данного типа (колонка 3). */
  count: number;
  /** Список измеренных факторов (минимум 1). */
  factors: SummaryFactor[];
}

/** Раздел таблицы (АУП / производственный персонал и т.п.). */
export interface SummaryPlace {
  /** Номер раздела (1, 2, …). */
  number: number;
  /** Заголовок раздела. */
  name: string;
  /** Рабочие места внутри раздела. */
  workplaces: SummaryWorkplace[];
}

export interface SummaryProtocol {
  protocol: {
    /** Номер протокола, напр. "1004-СВД". */
    number: string;
    /** Год оформления, напр. "2026". */
    year: string;
    /** День даты в шапке. */
    day: string;
    /** Месяц (родительный падеж), напр. "апреля". */
    month: string;
    /** Год даты в шапке. */
    dateYear: string;
  };
  customer: {
    name: string;
    address: string;
  };
  /** Место(а) проведения измерений (свободный текст с разделами). */
  measurementLocation: string;
  measurementDate: {
    day: string;
    month: string;
    year: string;
  };
  /** Характеристика помещения. */
  roomDescription: string;
  /** Система коллективной защиты ("имеется" / "отсутствует"). */
  collectiveProtection: string;
  /** Виды оборудования и их количество. */
  equipment: string;
  /** Перечень наименований профессий и должностей. */
  professionsList: string;
  /** Средства измерения (таблица СИ). */
  measuringTools: SummaryMeasuringTool[];
  /** НД на объект (длинный текст с приказом + ГОСТами). */
  productStandard: string;
  /** Условия окружающей среды на момент измерений. */
  conditions: {
    /** Температура воздуха, ºС, напр. "16". */
    temperature: string;
    /** Относительная влажность, %, напр. "52". */
    humidity: string;
    /** Атмосферное давление, мм рт.ст., напр. "694". */
    pressure: string;
  };
  /** Разделы основной таблицы. */
  places: SummaryPlace[];
  performer: {
    fullName: string;
    position: string;
  };
  director: {
    fullName: string;
    position: string;
  };
}
