/**
 * Документ №12 — «Оценка травмобезопасности».
 *
 * В исходном DOCX это одна большая таблица из 7 колонок, в которой
 * строки сгруппированы заголовками-разделами вида:
 *   «1. Административно – управленческий персонал»
 *   «2. Производственный персонал»
 * и далее идут карточки рабочих мест (код, должность, количество,
 * оборудование, тех. документация, результат, причины несоответствия).
 *
 * Соответственно структура данных:
 *   SafetyProtocol → sections[] → rows[]
 */

/** Одна строка таблицы — одно рабочее место. */
export interface SafetyRow {
  /** Код рабочего места, напр. "01 001 001". */
  code: string;
  /** Наименование профессии/должности, напр. "Директор". */
  position: string;
  /** Количество рабочих мест данной профессии. */
  count: number;
  /**
   * Наименование оборудования, приспособлений и инструментов.
   * По исходнику обычно: "Оборудование согласно перечня".
   */
  equipment: string;
  /**
   * Наличие технической документации (паспорта, сертификата и др.).
   * По исходнику: "в наличии".
   */
  documentation: string;
  /**
   * Результат оценки травмобезопасности: "соответствует" /
   * "не соответствует".
   */
  result: string;
  /**
   * Причины несоответствия. Обычно "отсутствуют" либо текст причины.
   */
  nonComplianceReasons: string;
  /**
   * Свободный итоговый комментарий по строке (напр.
   * "соответствует стандартам").
   */
  finalNote: string;
}

/** Раздел таблицы — группа рабочих мест под общим заголовком. */
export interface SafetySection {
  /** Порядковый номер раздела (1, 2, …). */
  number: number;
  /**
   * Заголовок раздела как он попадёт в DOCX,
   * напр. "1. Административно – управленческий персонал".
   */
  title: string;
  /** Карточки рабочих мест внутри раздела. */
  rows: SafetyRow[];
}

export interface SafetyProtocol {
  protocol: {
    /** Номер протокола, напр. "1". */
    number: string;
  };
  customer: {
    name: string;
    address: string;
  };
  /**
   * Место проведения оценки (часто содержит перечисление
   * всех площадок через запятую).
   */
  measurementPlace: string;
  measurementDate: {
    day: string;
    month: string;
    year: string;
  };
  /** Разделы таблицы. */
  sections: SafetySection[];
  performer: {
    fullName: string;
    position: string;
  };
  representative: {
    fullName: string;
    position: string;
  };
}
