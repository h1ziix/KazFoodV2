/**
 * Документ №5 — «Протокол измерений параметров микроклимата
 * (метеофакторов)».
 *
 * В исходном DOCX это шапка протокола + одна большая таблица из
 * 12 колонок, строки которой сгруппированы заголовками-разделами
 * вида «1. Административно – управленческий персонал» / «2. Производственный
 * персонал». Каждая строка таблицы — одно рабочее место с измерениями
 * температуры, влажности, скорости воздуха и атмосферного давления.
 *
 * Структура данных:
 *   MeteoProtocol → places[] → measurements[]
 */

/** Одно измерение параметров микроклимата на одном рабочем месте. */
export interface MeteoMeasurement {
  /** Порядковый № п/п в таблице (колонка 1). */
  rowNumber: number;
  /** № точки по эскизу, напр. "1т" (колонка 2). */
  pointNumber: string;
  /** Место проведения / должность, напр. "Директор" (колонка 3). */
  place: string;
  /** Категория работ по тяжести: "Iб", "IIа", "IIб", "III" (колонка 4). */
  workCategory: string;
  /** Время суток проведения измерений: "день" / "ночь" (колонка 5). */
  timeOfDay: string;

  /** Температура измеренная, ºС (колонка 6), напр. "23,5". */
  tempMeasured: string;
  /** Температура допустимая по нормам, ºС (колонка 7), напр. "21-28". */
  tempAllowed: string;

  /** Влажность измеренная, % (колонка 8), напр. "42". */
  humidityMeasured: string;
  /** Влажность допустимая по нормам, % (колонка 9), напр. "60". */
  humidityAllowed: string;

  /** Скорость движения воздуха измеренная, м/с (колонка 10). */
  airSpeedMeasured: string;
  /** Скорость движения воздуха допустимая, м/с (колонка 11). */
  airSpeedAllowed: string;

  /** Атмосферное давление, мм рт.ст. (колонка 12), напр. "694". */
  pressure: string;
}

/** Раздел таблицы — группа рабочих мест под общим заголовком. */
export interface MeteoPlace {
  /** Порядковый номер раздела (1, 2, …). */
  number: number;
  /** Заголовок раздела, напр. "Административно – управленческий персонал". */
  name: string;
  /** Строки измерений внутри раздела. */
  measurements: MeteoMeasurement[];
}

export interface MeteoProtocol {
  protocol: {
    /** Номер протокола, напр. "1004-МЕТ". */
    number: string;
    /** Год оформления протокола, напр. "2026". */
    year: string;
    /** День даты в шапке, напр. "10". */
    day: string;
    /** Месяц даты в шапке (родительный падеж), напр. "апреля". */
    month: string;
    /** Год даты в шапке, напр. "2026". */
    dateYear: string;
  };
  customer: {
    name: string;
    address: string;
  };
  measurementDate: {
    day: string;
    month: string;
    year: string;
  };
  /** Цель измерений, напр. "Аттестация рабочих мест". */
  purpose: string;
  /**
   * Нормативный документ, по которому проводились измерения,
   * напр. "ГОСТ 30494-2011.".
   */
  methodologyStandard: string;
  /** НД на продукцию (объект), длинная фраза с приказом. */
  productStandard: string;
  /** Представитель заказчика, присутствовавший при измерениях. */
  representative: string;
  /** Характеристика помещения / количество работающих. */
  roomDescription: string;
  /**
   * Метеорологические условия в момент проведения измерений
   * (значения, попадающие в шапку «а) температура / б) влажность /
   * в) давление»).
   */
  conditions: {
    /** Температура воздуха снаружи/в момент, ºС, напр. "16". */
    t: string;
    /** Относительная влажность, %, напр. "52". */
    h: string;
    /** Атмосферное давление, мм рт.ст., напр. "694". */
    p: string;
  };
  /** Разделы таблицы (АУП / производственный персонал и т.д.). */
  places: MeteoPlace[];
  performer: {
    fullName: string;
    position: string;
  };
  director: {
    fullName: string;
  };
}
