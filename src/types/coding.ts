/**
 * Документ №13 — «Кодировка рабочих мест» (DOCX-источник «3. Кодировка»).
 *
 * Структура DOCX:
 *   1) Шапка «УТВЕРЖДАЮ» (директор / организация / ФИО / дата).
 *   2) Заголовок «Кодировка рабочих мест».
 *   3) Единая таблица из 3 столбцов: код / профессия / кол-во,
 *      строки сгруппированы по разделам с заголовочной строкой
 *      «1. Администрация — N рабочих мест», где N агрегируется
 *      из rows[].count → переиспользуется sumBy() из aggregate.ts.
 *   4) Финальная строка «Итого: M р/м», M = sum по всем разделам.
 *
 * Структура данных:
 *   CodingProtocol → approval + sections[] → rows[]
 *
 * Document-13 is a deliberate scalability stress-test for the rows/
 * sections registry pipeline: оно повторяет паттерн safety/siz
 * (sections+rows), но добавляет два новых элемента — derived count в
 * заголовке секции и grand total на хвосте. Оба вычисляются
 * декларативно в buildContext через sumBy() — никаких новых helpers
 * для DOCX-движка не потребовалось.
 */

/** Одна строка кодировки — одно рабочее место. */
export interface CodingRow {
  /**
   * Стабильная внутренняя идентичность строки (uuid). Назначается один раз
   * при создании строки и НИКОГДА не пересчитывается. Именно по нему (а не по
   * коду) зависимые протоколы матчатся при синхронизации — см.
   * syncWorkplaces.ts. Скрыт из формы. Optional только ради легаси-данных,
   * созданных до введения id: normalizeCodingDocument проставляет его.
   */
  id?: string;
  /**
   * Код рабочего места, например "01 001 001" — ПРОИЗВОДНОЕ отображаемое
   * значение, пересчитываемое из позиции: "01" (константный префикс) +
   * номер раздела + номер строки внутри раздела. Не идентичность: при
   * добавлении/удалении/перемещении строк коды перенумеровываются
   * (см. workplaceCodes.ts). Вручную не вводится.
   */
  code: string;
  /** Наименование рабочего места / профессии / оборудования. */
  name: string;
  /** Количество рабочих мест данной профессии. */
  count: number;
}

/** Раздел таблицы — группа строк под общим заголовком. */
export interface CodingSection {
  /**
   * Порядковый номер раздела (1, 2, …) — производное от позиции раздела в
   * массиве, пересчитывается normalizeCodingDocument. Вручную не вводится.
   */
  number: number;
  /** Базовый заголовок раздела без count, например
   *  "Административно – управленческий персонал". */
  title: string;
  /** Строки рабочих мест внутри раздела. */
  rows: CodingRow[];
}

export interface CodingProtocol {
  /** Блок «УТВЕРЖДАЮ» над таблицей. */
  approval: {
    /** Должность утверждающего, обычно "Директор". */
    position: string;
    /** Наименование организации, например "ТОО «KazEcoFood»". */
    organization: string;
    /** ФИО утверждающего, например "Балян Л.Н.". */
    fullName: string;
    /** Дата утверждения. */
    date: {
      day: string;
      month: string;
      year: string;
    };
  };
  /** Разделы кодировки (минимум один). */
  sections: CodingSection[];
}
