/**
 * Документ №14 — «Заключение / Отчёт по результатам обязательной
 * периодической аттестации производственных объектов по условиям
 * труда».
 *
 * Исходный DOCX (`14. Заключение KazFood.docx`) состоит из:
 *   1. Шапка лаборатории и название отчёта (kk / ru).
 *   2. Блок реквизитов: заказчик, место проведения, код рабочего
 *      места (текстовая ссылка), количество рабочих мест, дата
 *      проведения оценки.
 *   3. Итоговая таблица «Результаты оценки условий труда»:
 *      строки — производственные факторы (освещение, микроклимат,
 *      шум, ЭМП, тяжесть труда, напряжённость труда, общая оценка)
 *      плюс под-строки «мужчины / женщины» там, где это нужно;
 *      колонки — 6 классов условий труда: 2, 3.1, 3.2, 3.3, 3.4, 4.
 *      В каждой строке `count` помещается ровно в одну из 6 колонок,
 *      остальные остаются пустыми.
 *   4. Подписи: исполнитель (Оценку проводил), генеральный директор
 *      лаборатории (Согласовано), представитель организации.
 *
 * Структура модели максимально плоская: одна таблица rows[], где
 * каждый элемент — отдельная строка таблицы. Иерархия «мужчины /
 * женщины» под фактором задаётся последовательностью строк, а не
 * вложенными массивами, чтобы рендеринг оставался строго один-к-одному
 * с DOCX-шаблоном.
 */

/** Один из шести классов условий труда, либо "" если класс не назначен. */
export type ConclusionClass = "" | "2" | "3.1" | "3.2" | "3.3" | "3.4" | "4";

/**
 * Одна строка итоговой таблицы заключения.
 *
 * `count` — числовое значение (количество рабочих мест), помещаемое в
 * колонку, соответствующую `classValue`. Если `classValue === ""`, все
 * шесть классовых колонок остаются пустыми (например, для строк-
 * «заголовков» наподобие «Тяжесть труда:» без распределения).
 */
export interface ConclusionRow {
  /** Название фактора на казахском, напр. "Жарықтандыру". */
  labelKk: string;
  /** Название фактора на русском, напр. "Освещение". */
  labelRu: string;
  /** Класс условий труда для данной строки. */
  classValue: ConclusionClass;
  /** Количество рабочих мест с данным классом (или ""). */
  count: number | "";
}

export interface ConclusionProtocol {
  customer: {
    /** Наименование заказчика. */
    name: string;
    /** Адрес заказчика. */
    address: string;
  };
  /** Место(а) проведения оценки. */
  measurementPlace: string;
  /** Код(ы) рабочих мест — обычно ссылка на материалы аттестации. */
  workplaceCodeNote: string;
  /** Общее количество рабочих мест, напр. "55 мест". */
  totalWorkplaces: string;
  measurementDate: {
    day: string;
    month: string;
    year: string;
  };
  /** Строки итоговой таблицы. */
  rows: ConclusionRow[];
  /** Исполнитель ("Оценку проводил"). */
  performer: {
    fullName: string;
    position: string;
  };
  /** Подпись со стороны лаборатории ("Согласовано"). */
  laboratoryHead: {
    fullName: string;
    position: string;
  };
  /** Представитель организации-заказчика. */
  representative: {
    fullName: string;
    position: string;
  };
}
